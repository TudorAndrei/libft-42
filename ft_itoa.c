#include "./libft.h"

char	*ft_itoa(int n)
{
	size_t	i;
	char	*r;

	if (n == -2147483648)
		return (ft_strdup("-2147483648"));
	if (n == 2147483647)
		return (ft_strdup("2147483647"));
	i = 1 + ft_digitno(n);
	if (!(r = (char *)malloc(sizeof(char) * i)))
		return (NULL);
	if (n == 0)
		r[0] = '0';
	if (n < 0)
	{
		r[0] = '-';
		n = n * -1;
	}
	r[i - 1] = '\0';
	while (n > 0)
	{
		i--;
		r[i - 1] = (n % 10) + '0';
		n = n / 10;
	}
	return (r);
}
