#include "./libft.h"

int		ft_strnequ(char const *s1, const char *s2, size_t n)
{
	int		r;

	r = 0;
	if (s1 != NULL && s2 != NULL)
	{
		if (ft_strncmp(s1, s2, n) == 0)
			r = 1;
	}
	return (r);
}
