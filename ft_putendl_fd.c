#include "./libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char *s1;

	s1 = (char *)s;
	if (s)
	{
		ft_putstr_fd(s1, fd);
		ft_putchar_fd('\n', fd);
	}
}
